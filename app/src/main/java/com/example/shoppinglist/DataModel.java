package com.example.shoppinglist;

public class DataModel {

    String name;
    String version;
    int id;

    public DataModel(String name) {
        this.name = name;
        this.version = version;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}
